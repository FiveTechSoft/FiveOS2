#include <ClipApi.h>

#define MAKELONG(low, high) ((LONG)(((WORD)(low)) | (((DWORD)((WORD)(high))) << 16)))

//----------------------------------------------------------------------------//

CLIPPER nLoWord()
{
   _retni( ( unsigned int ) _parnl( 1 ) );
}

//----------------------------------------------------------------------------//

CLIPPER nHiWord()
{
   _retni( ( _parnl( 1 ) >> 16 ) & 0xFFFF );
}

//----------------------------------------------------------------------------//

CLIPPER nLoByte()
{
   _retni( _parni( 1 ) & 0x00FF );
}

//----------------------------------------------------------------------------//

CLIPPER nHiByte()
{
   _retni( ( _parni( 1 ) >> 8 ) & 0x00FF );
}

//----------------------------------------------------------------------------//

CLIPPER nMakeLong()
{
   _retnl( MAKELONG( _parni( 1 ), _parni( 2 ) ) );
}

